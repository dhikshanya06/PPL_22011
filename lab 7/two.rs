fn main() {
    let a: i32 = 17;
    let b: i32 = 6;

    println!("Addition: {} + {} = {}", a, b, a + b);
    println!("Subtraction: {} - {} = {}", a, b, a - b);
    println!("Multiplication: {} * {} = {}", a, b, a * b);
    println!("Division: {} / {} = {}", a, b, a / b);
    println!("Modulo: {} % {} = {}", a, b, a % b);
}

